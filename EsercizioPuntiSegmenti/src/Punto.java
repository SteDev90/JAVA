public class Punto {
	private double x, y, z;
	
	public Punto (double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public double getCoordinataX () {
		return this.x;
	}
	
	public double getCoordinataY () {
		return this.y;
	}
	
	public double getCoordinataZ () {
		return this.z;
	}
}
